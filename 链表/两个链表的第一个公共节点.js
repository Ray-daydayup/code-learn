/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */

/**
 * @param {ListNode} headA
 * @param {ListNode} headB
 * @return {ListNode}
 */
var getIntersectionNode = function (headA, headB) {
	let boy = headA
	let girl = headB
	while (boy !== girl) {
		boy = boy === null ? headB : boy.next
		girl = girl === null ? headA : girl.next
	}
	return boy
}
